#include <stdio.h>

int main() {
	int x, y, ans = 0;
	scanf("%d%d", &x, &y);
	for (int i = x; i <= y; ++i) {
		if (i % 400 == 0 || (i % 4 == 0 && i % 100 != 0))
			++ans;
	}
	printf("%d\n", ans);
	return 0;
}
