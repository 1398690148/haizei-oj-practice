#include<stdio.h>

int main() {
	int n;
	scanf("%d", &n);
	if(n <= 3) {
		printf("14\n");
	} else {
		double sum = 13 + (n - 3) * 2.3 + 1;
		printf("%g\n", sum);
	}
	return 0;
}
