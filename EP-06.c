#include <stdio.h>

int main() {
	int sum = 0, sum2 = 0;
	for (int i = 1; i <= 100; i++) {
		sum += i * i;
	}
	sum2 = (1 + 100) * 100 / 2;
	printf("%d\n", sum2 * sum2 - sum);
	return 0;
}
